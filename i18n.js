function t(obj, path) {
  return path.split('.').reduce((o, k) => (o && o[k] !== undefined ? o[k] : null), obj);
}

function setLanguage(lang) {
  if (!I18N[lang]) lang = 'en';
  const data = I18N[lang];
  localStorage.setItem('qgh-lang', lang);
  document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang === 'es' ? 'es-MX' : lang;

  document.title = data.metaTitle;
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc) metaDesc.content = data.metaDesc;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(data, el.dataset.i18n);
    if (val !== null) el.textContent = val;
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = t(data, el.dataset.i18nHtml);
    if (val !== null) el.innerHTML = val;
  });

  document.querySelectorAll('[data-i18n-list="pricing"]').forEach((el, i) => {
    if (data.pricing.inc[i]) el.textContent = data.pricing.inc[i];
  });

  document.querySelectorAll('[data-i18n-faq="q"]').forEach((el, i) => {
    if (data.faq.q[i]) {
      const arrow = el.querySelector('.arr');
      el.textContent = data.faq.q[i] + ' ';
      if (arrow) el.appendChild(arrow);
    }
  });

  document.querySelectorAll('[data-i18n-faq="a"]').forEach((el, i) => {
    if (data.faq.a[i]) el.querySelector('p').textContent = data.faq.a[i];
  });

  const tpl = document.getElementById('emailTemplate');
  if (tpl) tpl.textContent = data.apply.tpl;

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  const cur = document.getElementById('langCurrent');
  if (cur) cur.textContent = LANG_META[lang].label;

  window._currentLang = lang;
  window._i18n = data;
}

document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('qgh-lang');
  const browser = (navigator.language || 'en').slice(0, 2);
  const map = { en: 'en', ru: 'ru', zh: 'zh', es: 'es', ja: 'ja' };
  setLanguage(saved || map[browser] || 'en');

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
});
